                <div class="content">
                  <p style="text-align:justify;">
                    30 Oktober 2025, UPT BKN Gorontalo kembali menyelenggarakan Ujian Dinas Tingkat I, Tingkat II, dan Ujian Penyesuaian Ijazah bagi Pegawai Negeri Sipil (PNS) di Lingkungan Pemerintah Kabupaten Pohuwato. Kegiatan ini diikuti oleh 48 peserta dan menggunakan metode Computer Assisted Test (CAT) BKN. Pelaksanaan ujian berlangsung dengan tertib, lancar, dan sesuai dengan prosedur yang berlaku.
                  </p>

                  <p style="text-align:justify;">
                    Turut hadir dalam kegiatan tersebut Kepala Badan Kepegawaian dan Pengembangan Sumber Daya Manusia (BKPSDM) Kabupaten Pohuwato, Supratman Nento, S.IP., M.H. Dalam sambutannya, beliau menyampaikan rasa syukur dan apresiasi atas terselenggaranya kegiatan ini. “Kami sangat bersyukur dengan adanya fasilitasi Ujian CAT di Gorontalo. Ini sudah kedua kalinya Pemda Pohuwato mengikuti kegiatan serupa di UPT BKN Gorontalo. Kami berharap seluruh peserta dapat mengikuti ujian dengan sungguh-sungguh agar memperoleh hasil yang lebih baik,” ujar Supratman.
                  </p>

                  <p style="text-align:justify;">
                    Penyelenggaraan Ujian Dinas dan Penyesuaian Ijazah ini merupakan salah satu upaya BKN dalam mendukung peningkatan profesionalisme dan kompetensi ASN. Melalui sistem CAT, proses penilaian dilakukan secara transparan dan objektif, sehingga hasil yang diperoleh dapat mencerminkan kemampuan peserta. Dengan terlaksananya kegiatan ini, diharapkan ASN dapat terus meningkatkan kualitas kinerja serta memenuhi syarat administrasi kepegawaian sesuai dengan ketentuan yang berlaku.
                  </p>
                  
