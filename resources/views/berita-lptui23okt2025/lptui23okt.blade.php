                  </p>

                  <p style="text-align:justify;">
                    Kepala UPSCPKP BKN Gorontalo, Iskandar Zulkarnain Hadju menyampaikan apresiasi dan terima kasih kepada seluruh pihak yang telah berpartisipasi serta mendukung kelancaran kegiatan ini. Semoga hasil uji coba ini dapat memberikan kontribusi positif bagi penguatan sistem penilaian kompetensi di lingkungan ASN.
