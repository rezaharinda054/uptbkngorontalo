                <div class="content">
                  <p style="text-align:justify;">
                    Dalam rangka mendukung percepatan peningkatan Nilai Indeks Kualitas Data (IKD) Aparatur Sipil Negara (ASN) serta memperkuat pemenuhan Database Profil ASN secara komprehensif, Direktorat Pengeloaan Data dan Penyajian Informasi (PDPI) ASN Badan Kepegawaian Negara menyelenggarakan kegiatan “Asistensi Peningkatan Indeks Kualitas Data ASN untuk Mendukung Pemenuhan Database Profil ASN di Wilayah Kerja Kantor Regional XI BKN Manado”, pada Senin 20 Oktober 2025, bertempat di Kantor UPSCPKP BKN Gorontalo, Jl. Sultan Amay, Tamalate, Kota Gorontalo.
                  </p>

                  <p style="text-align:justify;">
                    Kegiatan ini dihadiri oleh Sekertaris Utama BKN, Direktur PDPI ASN, Kepala Kantor Regional XI BKN Manado, serta diikuti oleh 34 instansi di wilayah kerja Kanreg XI BKN baik secara luring maupun secara daring.
                  </p>
                  <p style="text-align:justify;">
                    Dari hasil evaluasi diperoleh bahwa Pemerintah Kabupaten Bone Bolango menempati peringkat pertama, disusul oleh Pemerintah Provinsi Maluku Utara dan Pemerintah Kabupaten Bolaang Mongondow sebagai instansi dengan capaian IKD tertinggi tahun 2025.
                  </p>

                  <p style="text-align:justify;">
                    Kegiatan ini bertujuan untuk memperkuat kolaborasi antarinstansi dalam mewujudkan data ASN yang akurat, mutakhir, dan terintegrasi, sebagai dasar penting dalam pelaksanaan kebijakan manajemen ASN yang profesional, transparan, dan berbasis data.
                  </p>

                  <p style="text-align:justify;">
                    Dengan adanya kegiatan asistensi diharapkan seluruh instansi pemerintah di wilayah kerja Kanreg XI BKN semakin meningkatkan komitmennya dalam pengelolaan dan pemutakhiran data ASN guna mendukung transformasi digital kepegawaian nasional.
                  </p>
                  
